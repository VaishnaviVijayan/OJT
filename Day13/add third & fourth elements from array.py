import numpy as np

array = np.array([1, 2, 3, 4, 5])

# Get the third and fourth elements (Note: array indices start from 0)
third_element = array[2]
fourth_element = array[3]

# Add the third and fourth elements
result = third_element + fourth_element

print(f"The third element is {third_element}")
print(f"The fourth element is {fourth_element}")
print(f"The sum of the third and fourth elements is {result}")
