def right_shift(num, n):
    return num >> n

# Example usage:
num = 60
n = 2
result = right_shift(num, n)
print(f"The result of right shifting {num} by {n} positions is {result}")

num = 100
n = 3
result = right_shift(num, n)
print(f"The result of right shifting {num} by {n} positions is {result}")
